package com.demo;

public class Switch {

	public static void main(String[] args) {
		char input = '@';// Enter any Character program
		
		switch (input)
		{
		case 'A' :
		case 'B' :
		case 'C' :
		case 'D' :
		{
			System.out.println("This is a Alphabet");
			break;// For Upper case Alphabets(A-D)
		}
		case '1' :
		case '2' :
		case '3' :
		case '4' :
		case '5' :
		{
			System.out.println(" This is a Digit");
			break;//For Digit(1-5)
		
	

}
}
}
}


